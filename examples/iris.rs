fn train<T, Tr>(mut trainer: Tr, epochs: usize) -> Tr::Model
where
    T: HyperVector,
    Tr: Trainer<T>,
{
    for epoch in 1..=epochs {
        let r = trainer.step(epoch);
        if r.errors == 0 {
            break;
        }
    }
    trainer.into_model()
}

fn run<T: HyperVector + Send + Sync>(data: &[T], labels: &[u8], rng: &mut impl Rng) {
    let epochs = 5;
    const NUM_CLASSES: usize = 3;
    let mut correct = 0;
    for i in 0..data.len() {
        // LOO
        let exclude = Some(i);
        const NUM_PROTOTYPES: usize = 3;
        let trainer = PerceptronMultiTrainer::<T, _>::new(
            data,
            labels,
            exclude,
            NUM_CLASSES,
            NUM_PROTOTYPES,
            &mut *rng,
        );

        let model = train(trainer, epochs);
        if model.predict(&data[i]) == labels[i] as usize {
            correct += 1;
        }
    }
    let acc = (100 * correct) as f64 / data.len() as f64;
    println!("Accuracy: {acc:.2}% ({correct}/{})", data.len());
}
